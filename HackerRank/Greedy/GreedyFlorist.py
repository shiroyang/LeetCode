# Complete the getMinimumCost function below.
def getMinimumCost(k, c):
    c.sort()
    rate = 1
    ans = 0
    
    while c:
        tmp = 0
        for _ in range(k):
            if c: tmp += c.pop()
        ans += tmp * rate
        rate += 1
    
    return ans
    
if __name__ == '__main__':
    fptr = open(os.environ['OUTPUT_PATH'], 'w')

    nk = input().split()

